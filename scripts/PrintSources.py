def is_int(a):
    try:
        a = int(a)
        return True
    except:
        return False

def format_is(mystr):
    outstr = "$^{"
    is_i = True
    for c in mystr:
        if is_int(c):
            outstr += c
        else:
            if is_i:
                is_i = False
                outstr += "}\\textrm{"
            outstr += c
    return outstr + "}$"

